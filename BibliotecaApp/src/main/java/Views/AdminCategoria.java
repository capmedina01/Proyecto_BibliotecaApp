    public void mostrarCategoria(){
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        List<Categoria> catEmpleados = catController.obtenerCat();
        for( Categoria catEmpleado : catEmpleados) {
            Object[] row = {
                catEmpleado.getCategoriaID(),
                catEmpleado.getNombre()
                
            };                
            
               model.addRow(row);
        }
    }
    
